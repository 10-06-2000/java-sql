CREATE TABLE OWNERS(
owner_id VARCHAR(10),
owner_name VARCHAR(20),
address VARCHAR(20),
phone_no BIGINT,
email_id  VARCHAR(20),
PRIMARY KEY(owner_id)
);
CREATE TABLE CUSTOMERS(
customer_id VARCHAR(10),
customer_name VARCHAR(20),
address VARCHAR(20),
phone_no BIGINT,
email_id  VARCHAR(20),
PRIMARY KEY(customer_id)
);
CREATE TABLE CARS(
car_id VARCHAR(10),
car_name VARCHAR(20),
car_type VARCHAR(10),
owner_id VARCHAR(20),
PRIMARY KEY(car_id)
);
CREATE TABLE RENTALS(
rental_id VARCHAR(10),
customer_id VARCHAR(10),
car_id VARCHAR(10),
pickup_date DATE,
return_date DATE,
km_driven INT,
fare_amount DOUBLE(10,2),
PRIMARY KEY(rental_id)
);
